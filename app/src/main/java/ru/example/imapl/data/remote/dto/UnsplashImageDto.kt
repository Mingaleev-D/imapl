package ru.example.imapl.data.remote.dto

import kotlinx.serialization.SerialName
import kotlinx.serialization.Serializable

@Serializable
data class UnsplashImageDto(
    val id: String,
    val description: String?,
    val height: Int,
    val width: Int,
    val urls: UrlsDto,
    val user: UserDto,
)

@Serializable
data class UrlsDto(
    val full: String,
    val raw: String,
    val regular: String,
    val small: String,
    val thumb: String
)

@Serializable
data class UnsplashImagesSearchResult(
    @SerialName("results")
    val images: List<UnsplashImageDto>,
    val total: Int,
    @SerialName("total_pages")
    val totalPages: Int
)

@Serializable
data class UserDto(
    val links: UserLinksDto,
    val name: String,
    @SerialName("profile_image")
    val profileImage: ProfileImageDto,
    val username: String
)

@Serializable
data class ProfileImageDto(
    val small: String
)

@Serializable
data class UserLinksDto(
    val html: String,
    val likes: String,
    val photos: String,
    val portfolio: String,
    val self: String
)


